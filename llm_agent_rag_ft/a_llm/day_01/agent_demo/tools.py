def add_tool(a,b):
    """
    让a+b，得到加法结果
    :param a: 第一个数字
    :param b: 第二个数字
    :return: a+b结果
    """
    return a + b

def sub_tool(a,b):
    """
    让a-b,得到减法结果
    :param a: a:第一个数字
    :param b: b:第二个数字
    :return: a-b结果
    """
    return a-b
def mul_tool(a,b):
    """
    让a*b,得到乘法结果
    :param a: a:第一个数字
    :param b: b:第二个数字
    :return: a*b结果
    """
    return a*b

def div_tool(a,b):
    """
    让a/b,得到除法结果
    :param a: a:第一个数字
    :param b: b:第二个数字
    :return: a/b结果
    """
    return a/b